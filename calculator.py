# First example

import math

def add(a, b):
    return a + b

def sub(a, b):
    return a - b

def mul(a, b):
    return a * b

def div(a, b):
    if b ==0:
        raise ZeroDivisionError("Cannot divide by zero!")
    return b/a


def log(a, b):
    try:
        return math.log(a,b)
    except ValueError:
        raise ValueError("Thatâ€™s not a valid number!")


def exp(a, b):
    return a**b





